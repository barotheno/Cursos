/**
 * OBJETOS:
 * Colecciones de datos (información)
 * que tienen un identificador y un valor.
 * 
 *      El valor seria:
 *          "Jose"
 * 
 *      El identificador, es el número por orden del valor empezando por 0
 */


// ARRAY (Arreglo)

["Jose", "José", "Juan", "Jean", "Oto"]

console.log(typeof ["Jose", "José", "Juan", "Jean", "Oto"])

// OBJETOS
/**
 * Tienen también identificadores y valores.
 * 
 *      A los identificadores se les llama atributos
 */

console.log(typeof {
    nombre: "Beto", //Nombre seria indentificador o atributo, "Beto" un valor
    segundoNombre: "Bustos"
})

// NULO Null
console.log(typeof null)